#include <locale.h>
#include <unistd.h>
#include "trace.h"

#include "ui/ui.h"

int
main(void)
{
    setlocale(LC_ALL, "");
    FILE *log = fopen("log", "a");
    tracer_init(TRC_DEBUG, TP_ALL, -1);
    tracer_add_stream(log);

    TRACE_INFO(" =========== FULL START ===========");

    ui_thread();

    TRACE_INFO(" ============ FULL STOP ============== ");

    usleep(1000);
    tracer_stop();
    fclose(log);
    return 0;
}

